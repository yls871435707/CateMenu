public class SeasonItem {
    private int headImg;
    private String nameTv;
    private String peopleTv;
    private String zanTv;

    public int getHeadImg() {
        return headImg;
    }

    public void setHeadImg(int headImg) {
        this.headImg = headImg;
    }

    public String getNameTv() {
        return nameTv;
    }

    public void setNameTv(String nameTv) {
        this.nameTv = nameTv;
    }

    public String getPeopleTv() {
        return peopleTv;
    }

    public void setPeopleTv(String peopleTv) {
        this.peopleTv = peopleTv;
    }

    public String getZanTv() {
        return zanTv;
    }

    public void setZanTv(String zanTv) {
        this.zanTv = zanTv;
    }
}
